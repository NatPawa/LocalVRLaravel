        $game->pathexe = "pathexe";
        $game->urllogo = "urllogo";
        $game->urlcover = "urlcover";
        return $game->save();
    }

    public function storelogo(Request $request)
    {
        $game = Game::all()->find($request->input('id'));
        $game->urllogo = $request->file('logo')->store('logos','public');
        $game->urlcover = $request->file('cover')->store('covers','public');
        $game->save();
        return ['message' => 'Game Images Saved'];     
    }

